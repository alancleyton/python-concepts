'''
In Python, the membership operators allow you to check the presence or absence in an object.
'''

# IN returns True if a sequence with the specified value is present in the object
name = 'John'
print('o' in name) # returns True
print('a' in name) # returns False

# NO IN returns True if a sequence with the specified value is not present in the object
print('a' not in name) # returns True
print('o' not in name) # returns False